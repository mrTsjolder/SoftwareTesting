package at.archkb.server.entity;

import org.neo4j.ogm.annotation.NodeEntity;

@NodeEntity
public class TradeoffItem extends GenericEntityImpl<TradeoffItem, Integer> {
	
	private static final long serialVersionUID = 1L;

	public TradeoffItem() {
		super(TradeoffItem.class);
	}

	private String name;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (!super.equals(obj)) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		TradeoffItem other = (TradeoffItem) obj;
		if (name == null) {
			if (other.name != null) {
				return false;
			}
		} else if (!name.equals(other.name)) {
			return false;
		}
		return true;
	}
}
